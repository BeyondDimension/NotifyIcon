namespace System.Windows;

/// <summary>
/// 定义一组可与工具提示相关联的标准化图标。
/// </summary>
public enum ToolTipIcon
{
    /// <summary>
    /// 不是标准图标。
    /// </summary>
    None,

    /// <summary>
    /// 信息图标。
    /// </summary>
    Info,

    /// <summary>
    /// 警告图标。
    /// </summary>
    Warning,

    /// <summary>
    /// 错误图标。
    /// </summary>
    Error,
}